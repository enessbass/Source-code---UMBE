function fonk() {
    var dddElement = document.querySelector('.gizli-box');
    
    if (dddElement.style.display === 'none' || dddElement.style.display === '') {
        dddElement.style.display = 'inline-block';
      } else {
        dddElement.style.display = 'none';
      }
  }

